package selfstudy;

public class BreakAndContinue {
	public static void main(String[] args) {
		int i;
		for(i = 0; i<=50 ; i++) {
			System.out.println(i);
			System.out.println("numbers in increasing order");	
		;
		if(i==26) {
			System.out.println("number not valid");
			break;
	
		}
				
	}
}
}